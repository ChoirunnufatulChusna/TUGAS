    private void SAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SAVEActionPerformed
        // TODO add your handling code here:
        try {
        int Kehadiran, Tugas, Nilai, UTS, UAS;
        
        Kehadiran = Integer.parseInt(hadir.getText())*10/100;
        Tugas = Integer.parseInt(tugass.getText()) *20/100;
        UTS = Integer.parseInt(uts.getText()) *25/100;
        UAS = Integer.parseInt(uas.getText()) *45/100;
        
        Nilai = Kehadiran + Tugas + UTS + UAS;
        
        if (Nilai > 90 && Nilai <=100) {
            Angka.setText(String.valueOf(Nilai));
            Huruf.setText("A");
        } else if (Nilai >= 85) {
            Angka.setText(String.valueOf(Nilai));
            Huruf.setText("B");
        } else if (Nilai >= 75) {
            Angka.setText(String.valueOf(Nilai));
            Huruf.setText("C");
        }else if (Nilai >= 65) {
            Angka.setText(String.valueOf(Nilai));
            Huruf.setText("D");
        } else if (Nilai >= 55) {
            Angka.setText(String.valueOf(Nilai));
            Huruf.setText("E");
        }else  {
            JOptionPane.showMessageDialog(null, "Inputkan Nilai 1 - 100");
        }
        
        
    }//GEN-LAST:event_SAVEActionPerformed
catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Inpukan Angka");
}
    }
